#Two types of type conversion implicit and Explicit Type Conversion
#Implicit : python automatically converts Data Type
#Explicit : user/developer force to convert data type


############### implicit type Conversion ###############

a = 2
b = 3.0
c =a+b
print(c)
print(type(c))

############### Explicit type Conversion ###############

age = "20"
age = int(age)
print(age)
print(type(age))